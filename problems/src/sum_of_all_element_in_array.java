// this is program to return sum of array .

public class sum_of_all_element_in_array {
    public static void main(String[] args) {
        int arr1[]={1,3,5,8,3};
        int sum=0;
        for(int i=0;i<arr1.length;i++){
            sum=sum+arr1[i];

        }
        System.out.println("Sum of array is "+sum);
    }
}
